import AppKit
import UniformTypeIdentifiers

/// JSON archive of all pasteboard item payloads — used when no single representation parses cleanly.
private struct PasteboardArchive: Codable {
    struct Entry: Codable {
        var type: String
        var data: Data
    }
    var entries: [Entry]
}

struct PasteboardSnapshot {
    var changeCount: Int
    var types: [NSPasteboard.PasteboardType]
    var string: String?
    var rtf: Data?
    var rtfd: Data?
    var png: Data?
    var tiff: Data?
    var fileURLs: [URL]
    var html: String?
    /// Passwords / concealed pasteboard markers (not Universal Clipboard).
    var isConcealed: Bool
    /// Momentary pasteboard content (automation, etc.) — skip unless Universal Clipboard.
    var isTransient: Bool
    /// Synced from another Apple device via Handoff / Universal Clipboard.
    var isUniversalClipboard: Bool
    var universalClipboardDeviceName: String?
    /// Full multi-type archive for lossless restore (see `PasteboardHelper.archiveMIME`).
    var archiveData: Data?
}

enum PasteboardHelper {
    static let concealedType = NSPasteboard.PasteboardType("org.nspasteboard.ConcealedType")
    static let autoGeneratedType = NSPasteboard.PasteboardType("org.nspasteboard.AutoGeneratedType")
    static let transientType = NSPasteboard.PasteboardType("org.nspasteboard.TransientType")
    /// Stored in `ClipboardItem.fileMIMEType` when `content` holds a `PasteboardArchive`.
    static let archiveMIME = "application/x-quietclipboard-pasteboard-archive"

    /// Refuse to ingest a single pasteboard representation above this size — avoids OOM kills.
    static let maxSingleTypeReadBytes = 128 * 1024 * 1024

    /// Main-thread snapshot: types, flags, and file URLs only. Heavy payloads are loaded later
    /// via `hydrateHeavyContent` on a background thread so a 50 MB image can't terminate the app.
    static func lightweightSnapshot(_ pb: NSPasteboard = .general) -> PasteboardSnapshot {
        metadataSnapshot(from: pb)
    }

    /// Full snapshot — used by paste-restore paths that already run off the hot capture loop.
    static func snapshot(_ pb: NSPasteboard = .general) -> PasteboardSnapshot {
        var snap = metadataSnapshot(from: pb)
        _ = hydrateHeavyContent(&snap, from: pb)
        return snap
    }

    private static func metadataSnapshot(from pb: NSPasteboard) -> PasteboardSnapshot {
        let types = pb.types ?? []

        var files: [URL] = []
        if let items = pb.pasteboardItems {
            for item in items {
                if let s = item.string(forType: .fileURL), let u = URL(string: s), u.isFileURL {
                    files.append(u)
                }
            }
        }

        let isUniversal = types.contains(UniversalClipboardBridge.remoteMarkerType)
        let concealed = types.contains(concealedType)
        let transient = types.contains(transientType)
        let autoGenerated = types.contains(autoGeneratedType)
        let origin = isUniversal ? UniversalClipboardBridge.origin(from: pb) : nil

        return PasteboardSnapshot(
            changeCount: pb.changeCount,
            types: types,
            string: nil,
            rtf: nil,
            rtfd: nil,
            png: nil,
            tiff: nil,
            fileURLs: files,
            html: nil,
            isConcealed: concealed && !isUniversal,
            isTransient: (transient || autoGenerated) && !isUniversal,
            isUniversalClipboard: isUniversal,
            universalClipboardDeviceName: origin?.displayName,
            archiveData: nil
        )
    }

    enum HydrationResult: Sendable {
        case ok
        case clipboardChanged
        case tooLarge
    }

    /// Loads bitmap/text/rich payloads off the main thread. Verifies `changeCount` so a fast
    /// double-copy doesn't ingest stale data.
    static func hydrateHeavyContent(_ snap: inout PasteboardSnapshot, from pb: NSPasteboard = .general) -> HydrationResult {
        guard pb.changeCount == snap.changeCount else { return .clipboardChanged }

        return autoreleasepool {
            let types = snap.types
            var sawTooLarge = false

            if types.contains(.string) {
                if let data = pb.data(forType: .string), !data.isEmpty {
                    if data.count > maxSingleTypeReadBytes {
                        sawTooLarge = true
                    } else if let s = pb.string(forType: .string), !s.isEmpty {
                        snap.string = capStoredText(s)
                    }
                }
            }

            if types.contains(.html) {
                if let data = pb.data(forType: .html), !data.isEmpty {
                    if data.count > maxSingleTypeReadBytes {
                        sawTooLarge = true
                    } else if let html = pb.string(forType: .html), !html.isEmpty {
                        snap.html = capStoredText(html)
                    }
                }
            }

            if types.contains(.rtf) {
                if let data = pb.data(forType: .rtf), !data.isEmpty {
                    if data.count > maxSingleTypeReadBytes { sawTooLarge = true }
                    else { snap.rtf = data.count > maxStoredRichBytes ? Data(data.prefix(maxStoredRichBytes)) : data }
                }
            }

            if types.contains(.rtfd) {
                if let data = pb.data(forType: .rtfd), !data.isEmpty {
                    if data.count > maxSingleTypeReadBytes { sawTooLarge = true }
                    else { snap.rtfd = data.count > maxStoredRichBytes ? Data(data.prefix(maxStoredRichBytes)) : data }
                }
            }

            if types.contains(.png) {
                snap.png = loadImageData(from: pb, type: .png, sawTooLarge: &sawTooLarge)
            } else if types.contains(.tiff) {
                snap.tiff = loadImageData(from: pb, type: .tiff, sawTooLarge: &sawTooLarge)
            }

            if !isBitmapDominant(types: types) {
                snap.archiveData = archiveData(from: pb)
            }

            if sawTooLarge { return .tooLarge }
            return .ok
        }
    }

    /// Hard limit on a single archive — prevents JSON-encoding (with base64 bloat) megabytes
    /// during paste-restore. Capture skips archival for bitmap-dominant pasteboards.
    static let archiveByteCap = 2 * 1024 * 1024
    private static let maxStoredTextChars = 500_000
    private static let maxStoredRichBytes = 8 * 1024 * 1024
    private static let maxStoredImageBytes = 8 * 1024 * 1024

    static func isBitmapDominant(types: [NSPasteboard.PasteboardType]) -> Bool {
        types.contains(.png) || types.contains(.tiff)
    }

    /// Caps stored plain text by character count (safe for grapheme clusters). Byte-size guard
    /// happens before `pb.string(forType:)` is called so we don't decode multi‑MB blobs.
    static func capStoredText(_ s: String) -> String {
        s.count > maxStoredTextChars ? String(s.prefix(maxStoredTextChars)) : s
    }

    /// Reads image bytes, downscaling immediately when the pasteboard blob is oversized so we
    /// don't retain a 40 MB TIFF alongside a decoded thumbnail.
    private static func loadImageData(
        from pb: NSPasteboard,
        type: NSPasteboard.PasteboardType,
        sawTooLarge: inout Bool
    ) -> Data? {
        guard let raw = pb.data(forType: type), !raw.isEmpty else { return nil }
        if raw.count > maxSingleTypeReadBytes {
            sawTooLarge = true
            return nil
        }
        if raw.count > maxStoredImageBytes {
            return ThumbnailGenerator.pngData(forImageData: raw, maxDimension: 2048) ?? raw
        }
        return raw
    }

    /// Captures every data payload on the pasteboard for lossless restore. Returns nil if the
    /// total payload exceeds `archiveByteCap` — restore is then silently skipped.
    static func archiveData(from pb: NSPasteboard = .general) -> Data? {
        let types = pb.types ?? []
        if isBitmapDominant(types: types) { return nil }

        var entries: [PasteboardArchive.Entry] = []
        var total = 0
        let skip: Set<String> = [
            concealedType.rawValue,
            transientType.rawValue,
            autoGeneratedType.rawValue
        ]
        for pasteItem in pb.pasteboardItems ?? [] {
            for type in pasteItem.types {
                guard !skip.contains(type.rawValue), let data = pasteItem.data(forType: type), !data.isEmpty else {
                    continue
                }
                if data.count > archiveByteCap { return nil }
                total += data.count
                if total > archiveByteCap { return nil }
                entries.append(PasteboardArchive.Entry(type: type.rawValue, data: data))
            }
        }
        guard !entries.isEmpty else { return nil }
        return try? JSONEncoder().encode(PasteboardArchive(entries: entries))
    }

    static func restoreArchive(_ data: Data, to pb: NSPasteboard = .general) -> Bool {
        guard let archive = try? JSONDecoder().decode(PasteboardArchive.self, from: data),
              !archive.entries.isEmpty else { return false }
        pb.clearContents()
        let item = NSPasteboardItem()
        for entry in archive.entries {
            item.setData(entry.data, forType: NSPasteboard.PasteboardType(entry.type))
        }
        return pb.writeObjects([item])
    }

    static func write(_ item: ClipboardItem, to pb: NSPasteboard = .general) {
        if item.fileMIMEType == archiveMIME, restoreArchive(item.content, to: pb) {
            return
        }

        pb.clearContents()
        switch item.contentType {
        case .text, .markdown, .code, .link, .color, .svg:
            if writeRichRepresentations(item, to: pb) { return }
            if let s = meaningfulPlainText(item) {
                pb.setString(s, forType: .string)
            }
        case .other:
            if writeRichRepresentations(item, to: pb) { return }
            if let s = meaningfulPlainText(item) {
                pb.setString(s, forType: .string)
            } else if !item.content.isEmpty {
                pb.setData(item.content, forType: .string)
            }
        case .richText:
            if writeRichRepresentations(item, to: pb) { return }
            if let s = meaningfulPlainText(item) {
                pb.setString(s, forType: .string)
            }
        case .image, .screenshot:
            writeImage(item.content, to: pb)
        case .file:
            if let s = item.textContent, let url = URL(string: s) {
                pb.writeObjects([url as NSURL])
            }
        }
    }

    /// Writes RTF / HTML / RTFD from stored content. Returns true if anything was written.
    private static func writeRichRepresentations(_ item: ClipboardItem, to pb: NSPasteboard) -> Bool {
        guard !item.content.isEmpty else { return false }
        switch item.fileMIMEType {
        case "text/html":
            if let html = String(data: item.content, encoding: .utf8) {
                pb.setString(html, forType: .html)
                if let s = meaningfulPlainText(item) { pb.setString(s, forType: .string) }
                return true
            }
        case "application/rtfd":
            pb.setData(item.content, forType: .rtfd)
            if let s = meaningfulPlainText(item) { pb.setString(s, forType: .string) }
            return true
        case "text/rtf":
            pb.setData(item.content, forType: .rtf)
            if let s = meaningfulPlainText(item) { pb.setString(s, forType: .string) }
            return true
        default:
            break
        }
        // Misclassified clips: try RTF/HTML parsing from raw bytes.
        if (try? NSAttributedString(
            data: item.content,
            options: [.documentType: NSAttributedString.DocumentType.rtf],
            documentAttributes: nil
        )) != nil {
            pb.setData(item.content, forType: .rtf)
            if let s = meaningfulPlainText(item) { pb.setString(s, forType: .string) }
            return true
        }
        if let html = String(data: item.content, encoding: .utf8),
           html.range(of: "<[a-zA-Z]", options: .regularExpression) != nil {
            pb.setString(html, forType: .html)
            if let s = meaningfulPlainText(item) { pb.setString(s, forType: .string) }
            return true
        }
        return false
    }

    /// Writes image bytes to the pasteboard. Fast path: if bytes are already PNG (signature
    /// `89 50 4E 47`), write them as-is — no NSImage round-trip on the main thread. Slow path
    /// only for legacy items that were mistagged as PNG while actually holding JPEG/TIFF bytes.
    private static func writeImage(_ data: Data, to pb: NSPasteboard) {
        if Self.isPNG(data) {
            pb.setData(data, forType: .png)
            return
        }
        guard let image = NSImage(data: data),
              let tiff = image.tiffRepresentation,
              let rep = NSBitmapImageRep(data: tiff) else {
            pb.setData(data, forType: .png)   // last resort: hand bytes through unchanged
            return
        }
        if let png = rep.representation(using: .png, properties: [:]) {
            pb.setData(png, forType: .png)
        }
        pb.setData(tiff, forType: .tiff)
    }

    private static func isPNG(_ data: Data) -> Bool {
        guard data.count >= 8 else { return false }
        return data[0] == 0x89 && data[1] == 0x50 && data[2] == 0x4E && data[3] == 0x47
    }

    private static func meaningfulPlainText(_ item: ClipboardItem) -> String? {
        guard let s = item.textContent?
            .trimmingCharacters(in: .whitespacesAndNewlines), !s.isEmpty else { return nil }
        return s
    }
}
